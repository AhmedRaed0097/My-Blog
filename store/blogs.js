export const mutations = {
    CREATENEWBLOG(state,payload){
        state.blogs.push(payload)
    },
    UPDATEBLOG(state,payload){
        state.blogs.forEach(blog => {
            if(blog.id === payload.id){
            state.blogs[state.blogs.indexOf(blog)] = payload
            }
        });
    },
    DELETEBLOG(state,payload){
        state.blogs.forEach(blog => {
            if(blog.id === payload){
            state.blogs.splice(state.blogs.indexOf(blog),1)
            }
        });
    },
}
export const actions = {
    createNewBlog({commit},payload){
        commit('CREATENEWBLOG',payload)
    },
    
    updateBlog({commit},payload){
        commit('UPDATEBLOG',payload)
    },
    deleteBlog({commit},payload){
        commit('DELETEBLOG',payload)
    },

}